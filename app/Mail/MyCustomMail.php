    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->from('bhagwatiprajapt2313@gmail.com')
                    // ->view('frontend.emails.verify_email')  // Create a corresponding Blade view
                    ->subject('Verify your email');
    }
    
}
